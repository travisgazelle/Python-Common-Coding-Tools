# Python does not have arrays
# Lists in Python are the equivalent of arrays
# I will demonstrate a list below

def main():
    colors = ['Red', 'Orange', 'Yellow', 'Green', 'Blue', 'Indigo', 'Violet']

    print('The colors of the rainbow include: ')
    for x in colors:
        print(x)

main()

